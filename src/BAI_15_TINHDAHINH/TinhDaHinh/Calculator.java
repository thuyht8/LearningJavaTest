package BAI_15_TINHDAHINH.TinhDaHinh;

public class Calculator {

    public int tinhTong(int number1, int number2){
        return number1 + number2;
    }

    public int tinhTong(int number1, int number2, int number3){
        return number1 + number2 + number3;
    }

    public float phepNhan (int number1, int number2){
        return number1 * number2;
    }

    public float phepNhan (int number1, int number2, int number3){
        return number1 * number2 * number3;
    }

}
